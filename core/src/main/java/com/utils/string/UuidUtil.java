package com.utils.string;

import java.util.UUID;

/**
 * 生成32位uuid
 * 
 */
public class UuidUtil {

	public static String get32UUID() {
		String uuid = UUID.randomUUID().toString().trim().replaceAll("-", "");
		return uuid;
	}
}

